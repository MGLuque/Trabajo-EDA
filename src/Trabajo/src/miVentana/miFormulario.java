    private void jMostrarClientesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMostrarClientesActionPerformed
        // TODO add your handling code here:
        String []titulo={"Nombre","RUC","Razon Social"};
        modelo=new DefaultTableModel(null, titulo);
        ArrayCola<ClienteCorporativo> objClientes=objG.verColaClientes();
        
        while(objClientes.colaVacia()==false){
            ClienteCorporativo c=objClientes.desencolar();
            String nombre=c.getNombre();
            int ruc=c.getRUC();
            String razon=c.getRazonSocial();
            
            String RUC=String.valueOf(ruc);
            
            modelo.addRow(new String []{nombre,RUC,razon});
        }jTableLista.setModel(modelo);
        objG.ReporteRecaudacion();
    }//GEN-LAST:event_jMostrarClientesActionPerformed

