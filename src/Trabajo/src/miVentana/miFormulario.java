    private void jApilarComprobanteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jApilarComprobanteActionPerformed
        //TODO add your handling code here:
        String nombre=jNombre.getText();
        String tipo=jTipo.getSelectedItem().toString();
        String ciudadO=jOrigen.getText();
        String ciudadD=jDestino.getText();
        int monto=Integer.parseInt(jMontoCliente.getText());
        int ruc=Integer.parseInt(jRUC.getText());
        String razonsocial=jRazonSocial.getText();
        Date objDate = new Date();
        String fecha= objDate.toString();
        CLIENTE=new ClienteCorporativo(nombre, ruc, razonsocial);
        refC= new Comprobante(tipo,ciudadO, ciudadD,  monto, fecha);
        CLIENTE.ApilarComprobante(refC);
        jNombre.setText("");
        jMontoCliente.setText("");
        jOrigen.setText("");
        jDestino.setText("");
        jRUC.setText("");
        jRazonSocial.setText("");
    }//GEN-LAST:event_jApilarComprobanteActionPerformed

